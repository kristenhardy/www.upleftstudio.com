	<h3>Why work in digital?</h3>
	<p>Well, I always start with sketching - pencil and paper (or biro and paper, or somebody's texta and the back of an envelope. It doesn't matter.) It takes a while, once you're a graphic designer, to realise no matter how good you get with the software, the important things are the ideas, the sketching, the research and refinement and working with your client.</p>
	<p>I like working with digital tools because I'm also a designer. I've worked with digital tools for about 10 years, and it's quicker.</p>
	<h3>What tools do you use?</h3>
	<p>Pencils</p>
	<p>Indian ink, with (size) nibs (messy)</p>
	<p>Watercolours - the cheap kind!</p>
	<p>Photoshop</p>
	<p>Illustrator</p>
	<p>The crappy old scanner</p>
	<p>A little Wacom tablet - still getting the hang of it</p>
	<p>I'm starting to learn After Effects</p>
