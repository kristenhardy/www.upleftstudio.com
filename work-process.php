<h2>My process</h2>
<p class="caption">Here's how I work through an illustration when I'm using digital tools.</p>
			
<div style="padding-top: 40px;" class="ten columns">

<p>1. I start with a sketch. Actually, lots of sketches. It's messy and rough. I would never show these sketches to anyone!</p>
<p>I spend a lot of time sketching. I reckon working through your ideas at this stage, and trying a bunch of other stuff, ends up with a better illustration at the end. This is also a good time to gather up some reference material.</p>
<img class="pin" src="../images/process/process-1-sketch.jpg">

<p>2. I scan the sketch. Now I work in Photoshop to refine my idea. It's starting to look clearer, although everything's still quite loose.</p>
<img class="pin" src="../images/process/process-2-trace.jpg">
<p class="small-caption">I work in pink so it's easy to see the lines. This was a trick I learnt from my illustration teacher while I was studying at TAFE.</p>
<p>3. Now I work on the sketch in Illustrator. I use the pen tool to create the basic shapes for the illustration. I start thinking about colour as well, although the colours for each element can be easily changed.</p>
<p><img class="pin" src="../images/process/process-3-illustrator.jpg"></p>
<p class="small-caption">This is probably the scariest step! It's not looking like much, really. Is it going to work out?</p>

<p>4. Back to Photoshop. The illustration really starts to take shape now.</p>
<p>I use the brush tool and my $20 Wacom tablet (thanks, Ebay) to render the elements.</p>
<img class="pin" src="../images/process/process-4-trace.jpg">
<p>5. I add in the background elements, shadows, and other elements.</p>
<p>Except for the first step, when I'm thinking of the concept, and sketching out ideas, this is where I spend the most time.</p>
<p>6. I'm done. Here's the final piece.</p>
<img class="pin" src="../images/process/process-5-joe.png">
<p>This illustration took about a day (a little less) from start to finish. 
"Joe was going faster and faster... He couldn't stop and danger was looming." Colour, composition and comedy were three elements for the brief.</p>






